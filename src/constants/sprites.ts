export interface SpriteLib {
  name: string;
  items: string[];
}

const _libs: SpriteLib[] = [];

function init(): SpriteLib[] {
  if (_libs.length > 0) return _libs;
  for (const entry of SPRITE_DATA) {
    const colon = entry.indexOf(":");
    const libName = entry.substring(0, colon);
    const last = _libs[_libs.length - 1];
    if (last && last.name === libName) {
      last.items.push(entry);
    } else {
      _libs.push({ name: libName, items: [entry] });
    }
  }
  return _libs;
}

export function getSpriteLibs(): SpriteLib[] {
  return init();
}

export function getSpriteLibByIndex(index: number): SpriteLib | undefined {
  return init()[index];
}
